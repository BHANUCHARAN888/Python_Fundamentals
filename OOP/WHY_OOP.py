# Before OOP, we mainly used Procedural Programming.

# Procedural programming consists of:
# --> Variables
# --> Functions
# --> Function calls

# This approach works well for smaller applications.

# But when applications become larger:
# --> Data and functions become separated
# --> Managing code becomes difficult
# --> Complexity increases
# --> Code reusability decreases
# --> Making changes becomes harder

# OOP solves these problems by combining:

# DATA + FUNCTIONS = OBJECT

# Classes act as blueprints to create objects
# Objects represent real-world entities with:
# --> Attributes (data)
# --> Methods (functions)